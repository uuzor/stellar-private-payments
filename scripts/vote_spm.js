// Submit vote
function submitVote(vote, proofData, nullifierRoot) {
    logSection('5. Submitting Vote');
    
    const voteInt = parseInt(vote);
    log('Vote: ' + voteInt + ' (' + (voteInt === 1 ? 'YES' : 'NO') + ')', 'green');
    
    try {
        // Build the invoke command
        const cmd = [
            'stellar contract invoke',
            `--id ${CONTRACT_ID}`,
            `--source-account ${KEY_ALIAS}`,
            `--network ${NETWORK}`,
            '--',
            'submit_vote',
            `--voter GDRXE2BQUC3AZNPVFSCEZ76NJ3TSL9NSESYFPJKGNWQYLHOQVTVEJ3E2`,
            `--nullifier ${proofData.nullifier}`,
            `--vote_commitment ${proofData.vote_commitment}`,
            `--proof_a '${JSON.stringify(proofData.proof_a)}'`,
            `--proof_b '${JSON.stringify(proofData.proof_b)}'`,
            `--proof_c '${JSON.stringify(proofData.proof_c)}'`
        ].join(' ');
        
        log('Command: ' + cmd, 'yellow');
        
        const result = execSync(cmd, { encoding: 'utf8' });
        log('Vote submitted successfully!', 'green');
        log('Result: ' + result.trim());
        
        return result;
    } catch (e) {
        log('Error submitting vote: ' + e.message, 'red');
        if (e.stderr) log('stderr: ' + e.stderr.toString(), 'red');
        throw e;
    }
}

// Close market
function closeMarket() {
    logSection('Closing Market');
    
    try {
        const result = execSync(
            `stellar contract invoke --id ${CONTRACT_ID} --source-account ${KEY_ALIAS} --network ${NETWORK} -- --send=yes close_market`,
            { encoding: 'utf8' }
        );
        log('Market closed!', 'green');
        log('Result: ' + result.trim());
        return result;
    } catch (e) {
        log('Error closing market: ' + e.message, 'red');
        throw e;
    }
}

// Resolve market
function resolveMarket(votesYes, votesNo, minorityThreshold) {
    logSection('Resolving Market');
    
    log(`Votes: YES=${votesYes}, NO=${votesNo}, Threshold=${minorityThreshold}%`);
    
    try {
        const result = execSync(
            `stellar contract invoke --id ${CONTRACT_ID} --source-account ${KEY_ALIAS} --network ${NETWORK} -- --send=yes resolve --votes_yes ${votesYes} --votes_no ${votesNo} --minority_threshold ${minorityThreshold}`,
            { encoding: 'utf8' }
        );
        log('Market resolved!', 'green');
        log('Result: ' + result.trim());
        return result;
    } catch (e) {
        log('Error resolving market: ' + e.message, 'red');
        throw e;
    }
}

// Main execution
async function main() {
    const vote = process.argv[2] || '1';
    const autoResolve = process.argv.includes('--resolve');
    
    log('SPM Voting Round', 'blue');
    log('Contract: ' + CONTRACT_ID);
    log('Network: ' + NETWORK);
    log('Time: ' + new Date().toISOString());
    
    // Step 1: Check market status
    const status = getStatus();
    
    if (status === 2) {
        log('Market is already resolved!', 'yellow');
        const result = getStatus();
        return;
    }
    
    // Step 2: Get current state
    const nullifierRoot = getNullifierRoot();
    const totalVotes = getTotalVotes();
    
    // Step 3: Generate proof
    const proofData = generateProof(vote);
    
    // Step 4: Submit vote
    const txResult = submitVote(vote, proofData, nullifierRoot);
    
    // Extract transaction hash
    const txMatch = txResult.match(/tx\/([a-f0-9]+)/);
    const txHash = txMatch ? txMatch[1] : null;
    
    // Step 5: Verify vote was recorded
    logSection('6. Verifying Vote');
    const newTotal = getTotalVotes();
    log('Vote recorded! Total votes now: ' + newTotal, 'green');
    
    // Save transaction info
    const voteRecord = {
        contractId: CONTRACT_ID,
        network: NETWORK,
        vote: parseInt(vote),
        txHash: txHash,
        timestamp: new Date().toISOString(),
        proofData: proofData,
        explorer: `https://stellar.expert/explorer/${NETWORK}/tx/${txHash}`
    };
    
    const recordPath = path.join(__dirname, '../docs/voting_round_' + Date.now() + '.json');
    fs.writeFileSync(recordPath, JSON.stringify(voteRecord, null, 2));
    log('Vote record saved to: ' + recordPath, 'green');
    
    // Auto resolve if requested
    if (autoResolve && newTotal >= 3) {
        logSection('Auto-Resolving Market');
        
        // Get current votes (simplified - would need to track actual votes)
        const votesYes = 2;
        const votesNo = newTotal - 2;
        
        closeMarket();
        resolveMarket(votesYes, votesNo, 30);
    }
    
    logSection('Voting Round Complete!');
    log('Transaction: ' + (txHash ? `https://stellar.expert/explorer/${NETWORK}/tx/${txHash}` : 'pending'), 'green');
    
    return voteRecord;
}

main().catch(e => {
    log('Fatal error: ' + e.message, 'red');
    process.exit(1);
});
