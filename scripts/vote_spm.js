/**
 * SPM Voting Script
 * 
 * Run a complete voting round on testnet:
 * 1. Get current nullifier root
 * 2. Generate ZK proof
 * 3. Submit vote to contract
 * 
 * Usage: node vote_spm.js <vote> [--network testnet|futurenet]
 *   vote: 0 (no) or 1 (yes)
 */

const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

// Configuration
const CONTRACT_ID = 'CCQ3T4VKYXBGJQ3BXPUCKOW4CDFQ5YR4UY4N54QY4EJOWC2NEYIJJZQI';
const NETWORK = process.argv.includes('--futurenet') ? 'futurenet' : 'testnet';
const KEY_ALIAS = 'spm-deployer';

// Colors for output
const colors = {
    reset: '\x1b[0m',
    green: '\x1b[32m',
    yellow: '\x1b[33m',
    blue: '\x1b[34m',
    red: '\x1b[31m'
};

function log(message, color = 'reset') {
    console.log(`${colors[color]}${message}${colors.reset}`);
}

function logSection(title) {
    console.log(`\n${'='.repeat(60)}`);
    log(title, 'blue');
    console.log('='.repeat(60));
}

// Get current nullifier root from contract
function getNullifierRoot() {
    logSection('1. Getting Current Nullifier Root');
    
    try {
        const result = execSync(
            `stellar contract invoke --id ${CONTRACT_ID} --source-account ${KEY_ALIAS} --network ${NETWORK} -- get_nullifier_root`,
            { encoding: 'utf8' }
        );
        log('Current nullifier root: ' + result.trim(), 'green');
        return result.trim();
    } catch (e) {
        log('Error getting nullifier root: ' + e.message, 'red');
        // Return empty root for initial vote
        return '0';
    }
}

// Get current market status
function getStatus() {
    logSection('2. Getting Market Status');
    
    try {
        const result = execSync(
            `stellar contract invoke --id ${CONTRACT_ID} --source-account ${KEY_ALIAS} --network ${NETWORK} -- get_status`,
            { encoding: 'utf8' }
        );
        const statusCode = parseInt(result.trim());
        const statuses = ['Open', 'Closed', 'Resolved'];
        log('Market status: ' + statuses[statusCode] + ' (' + statusCode + ')', 'green');
        return statusCode;
    } catch (e) {
        log('Error getting status: ' + e.message, 'red');
        return -1;
    }
}

// Get total votes
function getTotalVotes() {
    logSection('3. Getting Vote Count');
    
    try {
        const result = execSync(
            `stellar contract invoke --id ${CONTRACT_ID} --source-account ${KEY_ALIAS} --network ${NETWORK} -- get_total_votes`,
            { encoding: 'utf8' }
        );
        log('Total votes: ' + result.trim(), 'green');
        return parseInt(result.trim());
    } catch (e) {
        log('Error getting votes: ' + e.message, 'red');
        return 0;
    }
}

